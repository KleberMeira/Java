package exerciciodois;

public class Animal {

	private String raca;
	private int idade;
	
	public Animal() {
		
	}
	
	public Animal(String raca, int idade) {
		super();
		this.raca = raca;
		this.idade = idade;
	}

	public String getRaca() {
		return raca;
	}
	public int getIdade() {
		return idade;
	}
	
	
	
	
}
