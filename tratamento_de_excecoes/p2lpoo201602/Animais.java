package p2lpoo201602;

public class Animais {

	private String raca;
	private int idade;
	
		
	public Animais(String raca, int idade) {
		super();
		this.raca = raca;
		this.idade = idade;
	}
	
	
	public String getRaca() {
		return raca;
	}
	public void setRaca(String raca) {
		this.raca = raca;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
}
